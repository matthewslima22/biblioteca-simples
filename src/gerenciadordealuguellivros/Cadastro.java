    private void jTableTabelaUsuariosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableTabelaUsuariosMouseClicked
        String nome_usuario =""+jTableTabelaUsuarios.getValueAt(jTableTabelaUsuarios.getSelectedRow(), 1);
        conex.conexao();
        conex.executaSql("select *from usuario where nome_usuario='"+nome_usuario+"'");
        try {
            conex.rs.first();
            jTextFieldCodigo.setText(String.valueOf(conex.rs.getInt("cod_usuario")));
            jTextFieldNome.setText(conex.rs.getString("nome_usuario"));
            jComboBoxLivros.setSelectedIndex(conex.rs.getInt("livros_usuario"));
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao Selecionar Dados\n"+ex);
            
        }
        
        conex.desconecta();
        jButtonEditar.setEnabled(true);
        jButtonExcluir.setEnabled(true);
    }//GEN-LAST:event_jTableTabelaUsuariosMouseClicked

    public void preencherTabela(String Sql){
        ArrayList dados = new ArrayList();
        String[] colunas = new String []{"Codigo","Nome de usuario", "Livro", "Valor"};
        conex.conexao();
        conex.executaSql(Sql);
        
        try{
            conex.rs.first();
            do{
                dados.add(new Object[]{conex.rs.getInt("cod_usuario"),conex.rs.getString("nome_usuario"),conex.rs.getInt("livros_usuario"),conex.rs.getInt("valor_em_reais")});
            }while(conex.rs.next());
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(rootPane, "Erro ao Preencher ArrayList"+ex);
        }       
        ModeloTabela modelo = new ModeloTabela(dados, colunas);
        
        jTableTabelaUsuarios.setModel(modelo);
        jTableTabelaUsuarios.getColumnModel().getColumn(0).setPreferredWidth(60);
        jTableTabelaUsuarios.getColumnModel().getColumn(0).setResizable(false);
        jTableTabelaUsuarios.getColumnModel().getColumn(1).setPreferredWidth(220);
        jTableTabelaUsuarios.getColumnModel().getColumn(1).setResizable(false);
        jTableTabelaUsuarios.getColumnModel().getColumn(2).setPreferredWidth(60);
        jTableTabelaUsuarios.getColumnModel().getColumn(2).setResizable(false);
        jTableTabelaUsuarios.getColumnModel().getColumn(3).setPreferredWidth(60);
        jTableTabelaUsuarios.getColumnModel().getColumn(3).setResizable(false);
        
        jTableTabelaUsuarios.getTableHeader().setReorderingAllowed(false);
        jTableTabelaUsuarios.setAutoResizeMode(jTableTabelaUsuarios.AUTO_RESIZE_OFF);
        jTableTabelaUsuarios.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        
        conex.desconecta();
    }
            
